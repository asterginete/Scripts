function postGroup (group_link) {
	var mDelay1 = "CODE:WAIT SECONDS=15";
	var mDelay2 = "CODE:WAIT SECONDS=1";
	var mDelay3 = "CODE:WAIT SECONDS=50";
	var mGoto = "CODE:URL GOTO=" + group_link;
	var mThumbnail = "CODE:TAG POS=1 TYPE=A ATTR=CLASS:UIThumbPagerControl_Button<SP>UIThumbPagerControl_Button_Right";
	
	iimPlay(mGoto);
	
	if (window.content.find ("Write Post")) {
		var k = iCtr;
		var mTextAd = "CODE:TAG POS=1 TYPE=TEXTAREA FORM=ACTION:/ajax/updatestatus.php ATTR=NAME:xhpc_message CONTENT=" + content[iCtr];
		var j = 0;
		for (;;) {
			if (j == maxCtr) {
				break;
			}
			if (k == maxCtr) {
				k = 0;
			}
			if (window.content.find (keyword[k])) {
			}
			else {
				iimPlay(mDelay2);
				mTextAd = "CODE:TAG POS=1 TYPE=TEXTAREA FORM=ACTION:/ajax/updatestatus.php ATTR=NAME:xhpc_message CONTENT=" + content[k];
				iimPlay(mTextAd);
				iimPlay(mDelay1);				
				
				if (thumb[k] > 0) {
					for (var i = 0; i < thumb[k]; i++) {
						iimPlay(mThumbnail);
						iimPlay(mDelay2);
					}
				}
				
				var postID = getPostID();
				if (postID.length > 0) {
					var mPost = "CODE:TAG POS=1 TYPE=INPUT:SUBMIT ATTR=ID:" + getPostID();
					iimPlay(mPost);
				}
				iimPlay(mDelay3);
				
				iCtr++;
				if (iCtr == maxCtr) {
					iCtr = 0;
				}
				break;
			}
			k++;
			j++;
		}
	}
}

function getPostID()
{
	var x = window.content.document.getElementsByTagName("input");
	var postID = "";
	for (var j = 0; j < x.length; j++) {
		if ( (x[j].getAttribute("type") == "submit") && (x[j].getAttribute("value") == "Post") ) {
			postID = x[j].id;
		}
	}
	return postID;
}

